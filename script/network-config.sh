    5)
        echo -e "${YELLOW}[NET] Konfigurasi Custom...${NC}"
        read -p "Nama Jaringan       : " CUSTOM_NAME
        read -p "IP Debian           : " CUSTOM_IP
        read -p "Netmask             : " CUSTOM_MASK
        read -p "Gateway             : " CUSTOM_GW
        read -p "DNS Server          : " CUSTOM_DNS
        read -p "IP Proxmox          : " PROXMOX_IP
