set_static_ip() {
    local IP=$1
    local GATEWAY=$2
    local DNS=$3
    local NETMASK=$4
    local PROXMOX=$5
    local NETWORK_NAME=$6

    cat > /etc/network/interfaces << EOF
# Auto generated by network-config.sh
# Jaringan  : $NETWORK_NAME
# Tanggal   : $(date '+%Y-%m-%d %H:%M:%S')
# Auto Server Provisioning - farahamimah.net

source /etc/network/interfaces.d/*

auto lo
iface lo inet loopback

auto $INTERFACE
iface $INTERFACE inet static
    address $IP
    netmask $NETMASK
    gateway $GATEWAY
    dns-nameservers $DNS 8.8.8.8
EOF

    # Restart network
    ifdown $INTERFACE 2>/dev/null
    ifup $INTERFACE 2>/dev/null
    sleep 2

    # Update /etc/hosts
    sed -i "/server.farahamimah.net/d" /etc/hosts
    sed -i "/proxmox.farahamimah.net/d" /etc/hosts
    echo "$IP     server.farahamimah.net server" >> /etc/hosts
    echo "$PROXMOX proxmox.farahamimah.net proxmox" >> /etc/hosts

    echo -e "${GREEN}[NET] Jaringan   : $NETWORK_NAME${NC}"
    echo -e "${GREEN}[NET] IP Debian  : $IP${NC}"
    echo -e "${GREEN}[NET] IP Proxmox : $PROXMOX${NC}"
    echo -e "${GREEN}[NET] Gateway    : $GATEWAY${NC}"
    echo -e "${GREEN}[NET] Netmask    : $NETMASK${NC}"
    echo -e "${GREEN}[NET] DNS        : $DNS & 8.8.8.8${NC}"
}

set_dhcp() {
    cat > /etc/network/interfaces << EOF
# Auto generated by network-config.sh
# Mode: DHCP
# Tanggal: $(date '+%Y-%m-%d %H:%M:%S')

source /etc/network/interfaces.d/*

auto lo
iface lo inet loopback

auto $INTERFACE
iface $INTERFACE inet dhcp
EOF

    ifdown $INTERFACE 2>/dev/null
    ifup $INTERFACE 2>/dev/null
    sleep 3

    # Update /etc/hosts dengan IP baru
    IP_DHCP=$(hostname -I | awk '{print $1}')
    sed -i "/server.farahamimah.net/d" /etc/hosts
    echo "$IP_DHCP server.farahamimah.net server" >> /etc/hosts
    echo -e "${GREEN}[NET] DHCP aktif, IP: $IP_DHCP${NC}"
}

case $choice in
    1)
        echo -e "${YELLOW}[NET] Konfigurasi WiFi Rumah...${NC}"
        set_static_ip \
