            "$CUSTOM_IP" \
            "$CUSTOM_GW" \
            "$CUSTOM_DNS" \
            "$CUSTOM_MASK" \
            "$PROXMOX_IP" \
            "$CUSTOM_NAME"
        ;;
    6)
        echo -e "${YELLOW}[NET] Konfigurasi DHCP...${NC}"
        set_dhcp
        PROXMOX_IP="(cek manual)"
        ;;
    0)
        echo -e "${RED}Keluar...${NC}"
        exit 0
        ;;
    *)
        echo -e "${RED}[NET] Pilihan tidak valid!${NC}"
        exit 1
        ;;
esac

